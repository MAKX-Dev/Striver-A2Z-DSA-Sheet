#include<iostream>
using namespace std;
int main(){
    int day;
    cin>>day;
    switch(day){
        case1:cout<<"Monday"<<endl;
        break;
        case2:cout<<"Tuesday"<<endl;
        break;
        case3:cout<<"Wednusday"<<endl;
        break;
        case4:cout<<"Thursday"<<endl;
        break;
        case5:cout<<"Friday"<<endl;
        break;
        case6:cout<<"Saturay"<<endl;
        break;
        case7:cout<<"Sunday"<<endl;
        break;
        default:cout<<"Invalid"<<endl;
        break;
    }
    return 0;
}
